async function main() {
  const args = process.argv.slice(2);
  const validateOnly = args.includes('--validate');
  const validateVectorsFlag = args.includes('--validate-vectors');
  const targetFilter = args.find((a) => a.startsWith('--target='))?.split('=')[1];
  const skipQuicktype = args.includes('--skip-quicktype');
  VERBOSE = args.includes('--verbose') || args.includes('-v');

  console.log('üì¶ BuildIt Schema Code Generator\n');

  // Find all schema files (module + core schemas)
  const moduleSchemaFiles = await glob(`${MODULE_SCHEMAS_DIR}/**/v*.json`);
  const coreSchemaFiles = await glob(`${CORE_SCHEMAS_DIR}/**/v*.json`);
  const schemaFiles = [...moduleSchemaFiles, ...coreSchemaFiles];
  console.log(`Found ${schemaFiles.length} schema files (${moduleSchemaFiles.length} module, ${coreSchemaFiles.length} core)\n`);

  if (validateVectorsFlag) {
    await validateTestVectors();
    return;
  }

  if (validateOnly) {
    const valid = await validateSchemas(schemaFiles);
    if (!valid) {
      console.log('\n‚ùå Validation failed');
      process.exit(1);
    }
    console.log('\n‚úÖ All schemas valid');
    return;
  }

  // Collect module info for version tracking
  const moduleVersions: Record<string, string> = {};
  let totalTableCount = 0;

  // Pre-load all schemas so cross-module $refs can be resolved
  const allSchemas = new Map<string, ModuleSchema>();
  for (const file of schemaFiles) {
    const content = await readFile(file, 'utf-8');
    const schema: ModuleSchema = JSON.parse(content);
    const moduleName = basename(dirname(file));
    allSchemas.set(moduleName, schema);
  }

  // ‚îÄ‚îÄ Phase 1: Quicktype generation (existing) ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
  if (!skipQuicktype) {
    const activeTargets = targetFilter
      ? TARGETS.filter((t) => t.name === targetFilter)
      : TARGETS;

    for (const file of schemaFiles) {
      const moduleName = basename(dirname(file));
      const schema = allSchemas.get(moduleName)!;
      const isCore = file.includes('/core/');

      logv(`\nüìÑ Processing ${moduleName}${isCore ? ' (core)' : ''}...`);

      moduleVersions[moduleName] = schema.version;

      if (!schema.$defs || Object.keys(schema.$defs).length === 0) {
        logv(`  ‚è≠Ô∏è  No $defs found, skipping`);
        continue;
      }

      for (const target of activeTargets) {
        // Kotlin uses per-module subdirectories to match package structure
        const kotlinModuleName = moduleName.replace(/-/g, '_');
        const outputDir = target.lang === 'kotlin'
          ? join(target.outputDir, kotlinModuleName)
          : target.outputDir;
        await mkdir(outputDir, { recursive: true });

        try {
          const generatedCode = await generateAllTypesWithQuicktype(moduleName, schema, target.lang, allSchemas);
          const code = formatOutput(
            moduleName,
            schema.version || '1.0.0',
            schema.minReaderVersion || schema.version || '1.0.0',
            generatedCode,
            target.lang,
            isCore
          );
          const outputName = target.lang === 'rust' ? snakeCase(moduleName)
            : target.lang === 'kotlin' ? kotlinModuleName
            : moduleName;
          const outputFile = join(outputDir, `${outputName}${target.ext}`);
          await writeFile(outputFile, code);
          logv(`  ‚úÖ ${target.name}: ${outputName}${target.ext}`);
        } catch (error) {
          console.error(`  ‚ùå ${target.name}: ${error}`);
        }
      }
    }

    // Generate quicktype index files
    if (!targetFilter || targetFilter === 'typescript') {
      await generateTypeScriptIndex(schemaFiles);
    }
    if (!targetFilter || targetFilter === 'rust') {
      await generateRustMod(schemaFiles);
    }
  }

  // ‚îÄ‚îÄ Phase 2: Dexie DB schema generation (new) ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
  if (!targetFilter || targetFilter === 'typescript') {
    logv('\nüóÑÔ∏è  Generating Dexie DB schemas...');
    await mkdir(DEXIE_OUTPUT_DIR, { recursive: true });

    const dexieModules: string[] = [];

    for (const file of schemaFiles) {
      const content = await readFile(file, 'utf-8');
      const schema: ModuleSchema = JSON.parse(content);
      const moduleName = basename(dirname(file));

      moduleVersions[moduleName] = schema.version;

      const xStorage = schema['x-storage'];
      if (xStorage) {
        totalTableCount += Object.keys(xStorage.tables).length;
      }

      const dexieCode = generateDexieSchema(moduleName, schema);
      if (dexieCode) {
        const outputFile = join(DEXIE_OUTPUT_DIR, `${moduleName}.db.ts`);
        await writeFile(outputFile, dexieCode);
        dexieModules.push(moduleName);
        logv(`  ‚úÖ Dexie: ${moduleName}.db.ts`);
      }
    }

    await generateDexieIndex(dexieModules);
  }

  // ‚îÄ‚îÄ Phase 2b: SQLite migration generation ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
  if (!targetFilter || targetFilter === 'sqlite' || targetFilter === 'rust') {
    logv('\nüóÉÔ∏è  Generating SQLite migrations...');
    await mkdir(SQLITE_OUTPUT_DIR, { recursive: true });

    const sqliteModules: string[] = [];

    for (const file of schemaFiles) {
      const content = await readFile(file, 'utf-8');
      const schema: ModuleSchema = JSON.parse(content);
      const moduleName = basename(dirname(file));

      const sqlCode = generateSqliteMigration(moduleName, schema);
      if (sqlCode) {
        const outputFile = join(SQLITE_OUTPUT_DIR, `${moduleName}.sql`);
        await writeFile(outputFile, sqlCode);
        sqliteModules.push(moduleName);
        logv(`  ‚úÖ SQLite: ${moduleName}.sql`);
      }
    }

    // Generate a combined migration file for all modules
    if (sqliteModules.length > 0) {
      let combined = `-- Combined module tables migration\n`;
      combined += `-- Generated by tools/codegen from protocol schemas\n`;
      combined += `-- Modules: ${sqliteModules.join(', ')}\n\n`;

      for (const moduleName of sqliteModules) {
        const content = await readFile(join(SQLITE_OUTPUT_DIR, `${moduleName}.sql`), 'utf-8');
        combined += content + '\n';
      }

      await writeFile(join(SQLITE_OUTPUT_DIR, '_all_modules.sql'), combined);
      logv(`  ‚úÖ SQLite: _all_modules.sql (${sqliteModules.length} modules)`);
    }
  }

  // ‚îÄ‚îÄ Phase 3: Zod validation schema generation (new) ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
  if (!targetFilter || targetFilter === 'typescript') {
    logv('\n‚úÖ Generating Zod validation schemas...');
    await mkdir(ZOD_OUTPUT_DIR, { recursive: true });

    const zodModules: string[] = [];

    for (const file of schemaFiles) {
      const content = await readFile(file, 'utf-8');
      const schema: ModuleSchema = JSON.parse(content);
      const moduleName = basename(dirname(file));

      const zodCode = generateZodSchemas(moduleName, schema);
      if (zodCode) {
        const outputFile = join(ZOD_OUTPUT_DIR, `${moduleName}.zod.ts`);
        await writeFile(outputFile, zodCode);
        zodModules.push(moduleName);
        logv(`  ‚úÖ Zod: ${moduleName}.zod.ts`);
      }
    }

    await generateZodIndex(zodModules);
  }

  // ‚îÄ‚îÄ Phase 3b: Workers TypeScript + Zod generation ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
  if (!targetFilter || targetFilter === 'workers') {
    logv('\nüåê Generating Workers TypeScript schemas...');
    await mkdir(WORKERS_SCHEMAS_DIR, { recursive: true });
    await mkdir(WORKERS_ZOD_DIR, { recursive: true });

    const workersModules: string[] = [];
    const workersZodModules: string[] = [];

    for (const file of schemaFiles) {
      const content = await readFile(file, 'utf-8');
      const schema: ModuleSchema = JSON.parse(content);
      const moduleName = basename(dirname(file));

      if (!schema.$defs || Object.keys(schema.$defs).length === 0) continue;

      try {
        const generatedCode = await generateAllTypesWithQuicktype(moduleName, schema, 'typescript', allSchemas);
        const isCore = file.includes('/core/');
        const code = formatOutput(
          moduleName,
          schema.version || '1.0.0',
          schema.minReaderVersion || schema.version || '1.0.0',
          generatedCode,
          'typescript',
          isCore
        );
        await writeFile(join(WORKERS_SCHEMAS_DIR, `${moduleName}.ts`), code);
        workersModules.push(moduleName);
        logv(`  ‚úÖ Workers Schema: ${moduleName}.ts`);
      } catch (error) {
        console.error(`  ‚ùå Workers Schema ${moduleName}: ${error}`);
      }

      const zodCode = generateZodSchemas(moduleName, schema);
      if (zodCode) {
        await writeFile(join(WORKERS_ZOD_DIR, `${moduleName}.zod.ts`), zodCode);
        workersZodModules.push(moduleName);
        logv(`  ‚úÖ Workers Zod: ${moduleName}.zod.ts`);
      }
    }

    // Generate index files for workers
    if (workersModules.length > 0) {
      // Workers schemas don't have a suffix (files are just `moduleName.ts`),
      // so we generate a conflict-safe index by scanning the generated files directly.
      await generateWorkersSchemaIndex(WORKERS_SCHEMAS_DIR, workersModules);
      logv(`  ‚úÖ Workers: schemas/index.ts`);
    }

    if (workersZodModules.length > 0) {
      await generateConflictSafeIndex(
        WORKERS_ZOD_DIR,
        workersZodModules,
        'zod',
        'Workers Zod validation schemas from protocol schemas'
      );
      logv(`  ‚úÖ Workers: validation/index.ts`);
    }
  }

  // ‚îÄ‚îÄ Phase 4: Migration generation (new) ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
  if (!targetFilter || targetFilter === 'typescript') {
    logv('\nüîÑ Generating migrations...');
    await mkdir(MIGRATION_OUTPUT_DIR, { recursive: true });

    const { code: migrationCode, migrations } = await generateMigrations(MODULE_SCHEMAS_DIR);
    await writeFile(join(MIGRATION_OUTPUT_DIR, 'index.ts'), migrationCode);

    if (migrations.length > 0) {
      logv(`  ‚úÖ Generated ${migrations.length} migration(s)`);
    } else {
      logv(`  ‚ÑπÔ∏è  No version migrations needed (all modules at v1)`);
    }

    // Generate version file
    const versionCode = generateVersionFile(moduleVersions, totalTableCount);
    await writeFile(join(DEXIE_OUTPUT_DIR, 'version.ts'), versionCode);
    logv(`  ‚úÖ Version: version.ts (DB_SCHEMA_VERSION=${totalTableCount + 1})`);
  }

  // ‚îÄ‚îÄ Phase 5: Format generated TypeScript files with prettier ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
  logv('\nüíÖ Formatting generated files with prettier...');
  const formatDirs = [
    join(REPO_ROOT, 'clients/web/src/generated'),
    WORKERS_SCHEMAS_DIR,
    WORKERS_ZOD_DIR,
  ];
  for (const dir of formatDirs) {
    try {
      const proc = Bun.spawn(['bunx', 'prettier', '--write', '--log-level', 'warn', `${dir}/**/*.ts`], {
        cwd: REPO_ROOT,
        stdout: 'pipe',
        stderr: 'pipe',
      });
      await proc.exited;
    } catch {
      // prettier not available or failed - non-fatal
    }
  }
  logv('  ‚úÖ Formatting complete');

  console.log(`\n‚úÖ Code generation complete ‚Äî ${schemaFiles.length} schemas, ${Object.keys(moduleVersions).length} modules`);
}

main().catch(console.error);
